weather = 'sunny'
case (weather)
when 'cloudy'
puts "No need to be blue, rock your curls out and some suncreen, just in case!"
when 'foggy'
puts "Wear some flats and walk carefully"
when 'rainy'
puts "Grab your rain gear: umbrella, raincoat, and rainboots"
when 'cold'
puts "Do not forget your coat!"
when 'humid'
puts "Dress light and have some tissues handy"
when 'snowing'
puts "Hey, where are your hat and snow boots?"
when 'windy'
puts "Great time to tryout that new windbreaker"
else
puts "Hurray! Time to slap on some sunscreen, rock your shades, and let your curls pop!"
end